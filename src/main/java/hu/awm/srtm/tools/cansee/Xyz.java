package hu.awm.srtm.tools.cansee;

public class Xyz {
    private Double x;
    private Double y;
    private Double z;

    public Xyz(Double x, Double y, Double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public Double getX() {
        return x;
    }

    public Double getY() {
        return y;
    }

    public Double getZ() {
        return z;
    }
}
